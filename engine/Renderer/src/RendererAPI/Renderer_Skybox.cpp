	void loadSkybox(std::string dirName, void** skybox, char* flag)
	{
		*skybox = nullptr;
		*flag = 0;

		Skybox* tmpSkybox = new Skybox();

		loadCubemap(dirName, (void**)&(tmpSkybox->cubemap), flag);

		(*skybox) = (void*)tmpSkybox;

		*flag = 1;
	}
